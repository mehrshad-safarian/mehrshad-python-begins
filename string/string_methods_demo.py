# string_methods_demo.py

word1 = "python"
word2 = "1003"
word3 = "Save"
word4 = "Boolean"

print(word1, "is all letters? ->", word1.isalpha())
print(word2, "is all digits? ->", word2.isdigit())
print(word3, "is lowercase? ->", word3.islower())
print(word3, "is uppercase? ->", word3.isupper())
print(word4, "starts with 'B'? ->", word4.startswith("B"))
