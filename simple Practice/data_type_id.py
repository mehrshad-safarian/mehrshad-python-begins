''' For each of the following values determine data types:
42
3.14
"Hello, World!"
True
-7
"42"
False
0.0
'''
print(type(42)) #int
print(type(3.14)) #float
print(type("Hello, World!")) #str
print(type(True)) #Boolean (bool)
print(type(-7)) #int
print(type("42")) #str
print(type(False)) #Boolean (bool)
print(type(0.0)) #float
