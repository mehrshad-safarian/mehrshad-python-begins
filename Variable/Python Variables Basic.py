# Variables in Python

# assigning values
username = "m0023s"
score = 95
level = 3
is_active = True

print("Username:", username)
print("Score:", score)
print("Level:", level)
print("Active:", is_active)

# updating values
score = score + 10   # increase score
level += 1           # level up

print("\nAfter update:")
print("New Score:", score)
print("New Level:", level)

# combining variables
summary = f"Player {username} has {score} points at level {level}."
print("\nSummary:", summary)
